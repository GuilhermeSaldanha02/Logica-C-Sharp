//Variaveis
int numero1 = 10;
int numero2 = 20;

//Divisão
Console.WriteLine(numero1 / numero2);

//Multiplicação
Console.WriteLine(numero1 * numero2);

//Subtração
Console.WriteLine(numero1 - numero2);

//Soma
Console.WriteLine(numero1 + numero2);

//Incrementor
numero1++;
Console.WriteLine(numero1);

//Decrementor
numero2--;
Console.WriteLine(numero2);
  